#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	buf[12];
	char	*res;

	res = buf;
	res += 11;
	if (n == 0)
		ft_putchar_fd('0', fd);
	if (n >= 0)
	{
		while (n != 0)
		{
			*--res = '0' + (n % 10);
			n /= 10;
		}
	}
	else
	{
		while (n != 0)
		{
			*--res = '0' - (n % 10);
			n /= 10;
		}
		*--res = '-';
	}
	ft_putstr_fd(res, fd);
}
