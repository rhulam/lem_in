#include <stdlib.h>

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*str;
	int		i;

	i = 0;
	if (!s)
		return (NULL);
	if (!(str = (char*)malloc(sizeof(char) * len + 1)))
		return (NULL);
	len += start;
	while (start < len)
	{
		str[i++] = s[start++];
	}
	str[i] = '\0';
	return (str);
}
